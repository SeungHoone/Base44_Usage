function generateTrainScript(config) {
  const model = config.model || "resnet50";
  const dataset = config.dataset || "cifar10";
  const epochs = config.epochs || 100;
  const batchSize = config.batch_size || 32;
  const lr = config.learning_rate || 0.001;
  const checkpoint = config.checkpoint_path || "./checkpoints";

  return `#!/usr/bin/env python3
"""
Auto-generated training script
Model: ${model}  |  Dataset: ${dataset}
Generated by Vision Lab
"""

import torch
import torch.nn as nn
import torch.optim as optim
from torch.utils.data import DataLoader
import torchvision
import torchvision.transforms as transforms
import os

# ─── Config ───────────────────────────────────────────────────────────────────
MODEL_NAME   = "${model}"
DATASET_NAME = "${dataset}"
EPOCHS       = ${epochs}
BATCH_SIZE   = ${batchSize}
LEARNING_RATE = ${lr}
CHECKPOINT_DIR = "${checkpoint}"
DEVICE = "cuda" if torch.cuda.is_available() else "cpu"

os.makedirs(CHECKPOINT_DIR, exist_ok=True)

# ─── Dataset ──────────────────────────────────────────────────────────────────
transform = transforms.Compose([
    transforms.Resize(224),
    transforms.ToTensor(),
    transforms.Normalize([0.485, 0.456, 0.406],
                         [0.229, 0.224, 0.225]),
])

${dataset === "cifar10" ? `train_dataset = torchvision.datasets.CIFAR10(root="./data", train=True, download=True, transform=transform)
val_dataset   = torchvision.datasets.CIFAR10(root="./data", train=False, download=True, transform=transform)
NUM_CLASSES = 10` :
dataset === "cifar100" ? `train_dataset = torchvision.datasets.CIFAR100(root="./data", train=True, download=True, transform=transform)
val_dataset   = torchvision.datasets.CIFAR100(root="./data", train=False, download=True, transform=transform)
NUM_CLASSES = 100` :
dataset === "mnist" ? `transform = transforms.Compose([transforms.Resize(224), transforms.Grayscale(3), transforms.ToTensor()])
train_dataset = torchvision.datasets.MNIST(root="./data", train=True, download=True, transform=transform)
val_dataset   = torchvision.datasets.MNIST(root="./data", train=False, download=True, transform=transform)
NUM_CLASSES = 10` :
`# TODO: Replace with your dataset loader
# train_dataset = torchvision.datasets.ImageFolder("path/to/train", transform=transform)
# val_dataset   = torchvision.datasets.ImageFolder("path/to/val", transform=transform)
NUM_CLASSES = 1000  # Update to your number of classes
raise NotImplementedError("Set up your dataset above and remove this line")`}

train_loader = DataLoader(train_dataset, batch_size=BATCH_SIZE, shuffle=True,  num_workers=4, pin_memory=True)
val_loader   = DataLoader(val_dataset,   batch_size=BATCH_SIZE, shuffle=False, num_workers=4, pin_memory=True)

# ─── Model ────────────────────────────────────────────────────────────────────
${model.startsWith("resnet") ? `model = torchvision.models.${model.replace("-", "_")}(weights="DEFAULT")
model.fc = nn.Linear(model.fc.in_features, NUM_CLASSES)` :
model.startsWith("vgg") ? `model = torchvision.models.${model.replace("-", "_")}(weights="DEFAULT")
model.classifier[6] = nn.Linear(4096, NUM_CLASSES)` :
model.startsWith("efficientnet") ? `model = torchvision.models.${model.replace("-", "_")}(weights="DEFAULT")
model.classifier[1] = nn.Linear(model.classifier[1].in_features, NUM_CLASSES)` :
model === "mobilenet_v3" ? `model = torchvision.models.mobilenet_v3_large(weights="DEFAULT")
model.classifier[3] = nn.Linear(model.classifier[3].in_features, NUM_CLASSES)` :
`model = torchvision.models.resnet50(weights="DEFAULT")
model.fc = nn.Linear(model.fc.in_features, NUM_CLASSES)
# Note: ${model} — adjust the head replacement above accordingly`}
model = model.to(DEVICE)

# ─── Optimizer & Loss ─────────────────────────────────────────────────────────
criterion = nn.CrossEntropyLoss()
optimizer = optim.Adam(model.parameters(), lr=LEARNING_RATE)
scheduler = torch.optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=EPOCHS)

# ─── Training Loop ────────────────────────────────────────────────────────────
best_val_acc = 0.0

for epoch in range(1, EPOCHS + 1):
    # Train
    model.train()
    train_loss, correct, total = 0.0, 0, 0
    for inputs, labels in train_loader:
        inputs, labels = inputs.to(DEVICE), labels.to(DEVICE)
        optimizer.zero_grad()
        outputs = model(inputs)
        loss = criterion(outputs, labels)
        loss.backward()
        optimizer.step()
        train_loss += loss.item() * inputs.size(0)
        _, predicted = outputs.max(1)
        correct += predicted.eq(labels).sum().item()
        total += labels.size(0)

    train_acc  = 100.0 * correct / total
    train_loss = train_loss / total

    # Validate
    model.eval()
    val_loss, correct, total = 0.0, 0, 0
    with torch.no_grad():
        for inputs, labels in val_loader:
            inputs, labels = inputs.to(DEVICE), labels.to(DEVICE)
            outputs = model(inputs)
            loss = criterion(outputs, labels)
            val_loss += loss.item() * inputs.size(0)
            _, predicted = outputs.max(1)
            correct += predicted.eq(labels).sum().item()
            total += labels.size(0)

    val_acc  = 100.0 * correct / total
    val_loss = val_loss / total
    scheduler.step()

    print(f"Epoch [{epoch:3d}/{EPOCHS}]  "
          f"Train Loss: {train_loss:.4f}  Train Acc: {train_acc:.2f}%  "
          f"Val Loss: {val_loss:.4f}  Val Acc: {val_acc:.2f}%")

    # Save best checkpoint
    if val_acc > best_val_acc:
        best_val_acc = val_acc
        torch.save(model.state_dict(), os.path.join(CHECKPOINT_DIR, "best.pth"))
        print(f"  → Saved best model (val_acc={val_acc:.2f}%)")

print(f"\\nTraining complete. Best Val Acc: {best_val_acc:.2f}%")
`;
}

