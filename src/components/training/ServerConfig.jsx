function CodeBlock({ code, filename }) {
  const [copied, setCopied] = useState(false);

  const handleCopy = () => {
    navigator.clipboard.writeText(code);
    setCopied(true);
    setTimeout(() => setCopied(false), 2000);
  };

  const handleDownload = () => {
    const blob = new Blob([code], { type: "text/plain" });
    const a = document.createElement("a");
    a.href = URL.createObjectURL(blob);
    a.download = filename;
    a.click();
  };

  return (
    <div className="rounded-xl border border-white/10 bg-[#0a0a12] overflow-hidden">
      <div className="flex items-center justify-between px-4 py-2 border-b border-white/5 bg-white/[0.02]">
        <span className="text-[11px] font-mono text-slate-400">{filename}</span>
        <div className="flex gap-2">
          <button onClick={handleCopy} className="flex items-center gap-1 text-[10px] text-slate-500 hover:text-white transition-colors">
            {copied ? <Check className="w-3 h-3 text-emerald-400" /> : <Copy className="w-3 h-3" />}
            {copied ? "Copied!" : "Copy"}
          </button>
          <button onClick={handleDownload} className="flex items-center gap-1 text-[10px] text-slate-500 hover:text-white transition-colors">
            <Download className="w-3 h-3" />
            Download
          </button>
        </div>
      </div>
      <pre className="p-4 text-[11px] font-mono text-slate-300 overflow-x-auto max-h-80 overflow-y-auto leading-relaxed whitespace-pre">
        {code}
      </pre>
    </div>
  );
}

function NotebookCard({ nb }) {
  const isLocal = nb.localScheme !== null;

  const handleOpen = (e) => {
    e.preventDefault();
    if (nb.localScheme && (nb.localScheme.startsWith("http://localhost") || nb.localScheme.startsWith("vscode://"))) {
      window.open(nb.localScheme, "_blank");
    } else {
      window.open(nb.url, "_blank");
    }
  };

  return (
    <div
      className="group rounded-2xl border border-white/10 bg-white/[0.02] p-5 transition-all hover:border-white/20 hover:bg-white/[0.04] hover:scale-[1.02] cursor-pointer"
      onClick={handleOpen}
    >
      <div className="flex items-start justify-between mb-3">
        <div className={cn("w-8 h-8 rounded-lg bg-gradient-to-br flex items-center justify-center", nb.color)}>
          <BookOpen className="w-4 h-4 text-white" />
        </div>
        <div className="flex items-center gap-1.5">
          <span className={cn(
            "text-[10px] font-semibold px-2 py-0.5 rounded-full",
            isLocal ? "text-emerald-400 bg-emerald-500/10" : "text-slate-400 bg-white/5"
          )}>
            {nb.badge}
          </span>
          <ExternalLink className="w-3 h-3 text-slate-600 group-hover:text-slate-400 transition-colors" />
        </div>
      </div>
      <h4 className="font-semibold text-white text-sm">{nb.name}</h4>
      <p className="text-xs text-slate-400 mt-1">{nb.description}</p>
      {isLocal && (
        <p className="text-[10px] text-emerald-400 mt-2 flex items-center gap-1">
          <span className="w-1.5 h-1.5 rounded-full bg-emerald-400 animate-pulse" />
          Click to try opening locally
        </p>
      )}
    </div>
  );
}

